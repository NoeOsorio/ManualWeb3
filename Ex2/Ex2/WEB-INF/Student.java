package groups;

public class Student {

    String school_id;
    String name;
    String last_name;
    String subject;

    public Student( String school_id, String name, String last_name, String subject) {

        this.school_id = school_id;
        this.name = name;
        this.last_name = last_name;
        this.subject = subject;

    }
}
